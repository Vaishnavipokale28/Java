class StringReplace
{
	public static void main(String args[])
	{
		String s1="I am From SambhajiNagar";
		String replaceString=s1.replace('a','e');
		System.out.println(replaceString);
		String s2="My name is vaishnavi,She is very beautiful";
		String replace=s2.replace("is","was");
		System.out.println(replace);				
	}
}